# 4 operações com dois números
num1 = float(input("Digite um número: "))
num2 = float (input("Digite um segundo número: "))

soma = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2

print ("Soma: ", soma)
print ("Subtração: ", sub)
print ("Multiplicação: ", mult)
print ("Divisão: ", div)